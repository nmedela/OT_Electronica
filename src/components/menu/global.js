export default function PersistentDrawerLeft() {
  const classes = useStyles();
  const theme = useTheme();
  const [open, setOpen] = React.useState(false);
  const [title, setTitle] = React.useState('')

  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };
  const handleTitle = (_title) => {
    // setTitle(_title);
    handleDrawerClose()
  };

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar
        position="fixed"
        className={clsx(classes.appBar, {
          [classes.appBarShift]: open,
        })}
      >
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            edge="start"
            className={clsx(classes.menuButton, open && classes.hide)}
          >
            <MenuIcon />
          </IconButton>
          <Typography className={classes.title} variant="h6" noWrap>
            {title}
          </Typography>
          <div className={classes.search}>
            <div className={classes.searchIcon}>
              <SearchIcon />
            </div>
            <InputBase
              placeholder="Searchâ€¦"
              classes={{
                root: classes.inputRoot,
                input: classes.inputInput,
              }}
              inputProps={{ 'aria-label': 'search' }}
            />
          </div>
        </Toolbar>
      </AppBar>
      <Router basename="/">
        <Drawer
          className={classes.drawer}
          variant="persistent"
          anchor="left"
          open={open}
          classes={{
            paper: classes.drawerPaper,
          }}
        >
          <div className={classes.drawerHeader}>

            <IconButton onClick={handleDrawerClose}>
              {theme.direction === 'ltr' ? <ChevronLeftIcon /> : <ChevronRightIcon />}
            </IconButton>
          </div>
          <Divider />
          <List>
            {itemsMenu.map((item, index) => (
              <Link key={item.title} to={`${item.path}`} style={{ color: 'black', textDecoration: 'none' }}>
                <ListItem button onClick={() => { return handleTitle(item.title) }} key={item.title}>
                  <ListItemIcon><i className="material-icons">{item.icon}</i></ListItemIcon>
                  <ListItemText primary={item.title} />
                </ListItem>
              </Link>
            ))}
          </List>
          <Divider />
        </Drawer>
        <main
          className={clsx(classes.content, {
            [classes.contentShift]: open,
          })}
        >
          <div className={classes.drawerHeader} />
          <Switch>
            <Redirect
              exact
              from="/"
              to="/WO/new/-1" />
            {
              itemsMenu.map((item) => {
                return (
                  <Route key={item.title}
                    path={`${item.path}`}
                    render={(props) => {
                      setTitle(item.title)
                      return item.component(props)
                    }} />
                )
              })
            }
            <Route
              path='/lector/:id'
              render={(props) => {
                return <PdfViewer
                  {...props}
                  />
              }} />
            <Route
              path='/client/:id'
              render={(props) => {
                return <EditClient
                  {...props}
                  />
              }} />
          </Switch>

        </main>
      </Router>
    </div>
  );
}